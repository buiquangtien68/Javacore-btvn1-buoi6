public class Geometry {
    public void getPerimeter(Rectangle rectangle){
        double perimeter= (rectangle.length+rectangle.width)*2;
        System.out.println("Chu vi là: "+perimeter);
    };
    public void getAcreage(Rectangle rectangle){
        double acreage= rectangle.length* rectangle.width;
        System.out.println("Diện tích là: "+acreage);
    }




}
